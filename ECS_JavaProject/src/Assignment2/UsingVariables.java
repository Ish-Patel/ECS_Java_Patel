package Assignment2;

public class UsingVariables 
{
	public static void main(String[] args)
	{
		// Whole Variable
		int room;
		// Deciaml Variable
		double e;
		// Text Variable
		String ECS;
		
		// Assigning Variable Values
		room = 113;
		e = 2.71828;
		ECS = "Computer Science";
		
		// Printing Text In Console
		System.out.println( "This is room # " + room + "" );
		System.out.println( "e is close to " + e + " " );
		System.out.println( "I am learning a bit about " + ECS + " " ); 
	}
}
